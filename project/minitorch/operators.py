def add(a: float, b: float) -> float:
    return a + b

def sub(a: float, b: float) -> float:
    return a - b

def mul(a: float, b: float) -> float:
    return a * b

def div(a: float, b: float) -> float:
    return a / b

def max(a: float, b: float) -> float:
    return a if a > b else b

def min(a: float, b: float) -> float:
    return a if a < b else b 